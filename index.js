function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}

function add(a, b){
  return a + b
}

function subtract(a, b){
  return b - a
}

function inc(n){
  return n++
}

function dec(n){
  return n--
}

function division(a, b){
  return divide(a, b)
}

function multiplication(a, b){
  return multiply(a, b)
}
