function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}

function addition(a, b){
  return add(a, b)
}

function subtractNum(a, b){
  return subtract(a, b)
}

function inc(n){
  return n++
}

function dec(n){
  return n--
}

function division(a, b){
  return divide(a, b)
}

function multiplication(a, b){
  return multiply(a, b)
}
